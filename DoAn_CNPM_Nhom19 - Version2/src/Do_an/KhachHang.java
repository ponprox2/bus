    public String laySoXe(String tk) {
        String soxe = "";
        Connection kn = layKetNoi();
        String Sql = "select BienSoXe \n"
                + "from NHANVIEN, CHUYENXE\n"
                + "where NHANVIEN.MaNV = CHUYENXE.MaNV\n"
                + "and NHANVIEN.MaNV=?\n"
                + "and NgayChay = ?";
        try {
            PreparedStatement ps = kn.prepareStatement(Sql);
            ps.setString(1, layMaNV(tk));
            ps.setString(2, LayNgay());
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                soxe = rs.getString(1);
            }
        } catch (SQLException e) {
            Logger.getLogger(KhachHang.class.getName()).log(Level.SEVERE, null, e);
        }
        return soxe;

    }

