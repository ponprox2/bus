    public int LayMaVe(int maloai) {
        int mave = 0;
        String a = null;
        String c = SoXe.getText();
        Connection kn = layKetNoi();

        String sql = "SELECT MAX(Mave) FROM VEXE,CHUYENXE where MaLoaiVe = ? \n"
                + "and VEXE.MaChuyenXe=CHUYENXE.MaChuyenXe\n"
                + "and CHUYENXE.MaChuyenXe= ? ";
        try {
            PreparedStatement ps = kn.prepareStatement(sql);
            ps.setInt(1, maloai);
            ps.setInt(2, layMaChuyenXe(SoXe.getText()));
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                mave = rs.getInt(1);
            }
        } catch (SQLException e) {
            Logger.getLogger(KhachHang.class.getName()).log(Level.SEVERE, null, e);
        }

        if (maloai == 1 && mave == 0) {
            mave = 0;
        }

        if (maloai == 2 && mave == 0) {
            mave = 5000;
        }
        
        String ChMave;
        if (mave == 0 || mave == 5000) {
            if (mave == 0) {
                ChMave = layMaChuyenXe(c) + "000" + mave;
                mave = Integer.parseInt(ChMave);

            } else {
                ChMave = layMaChuyenXe(c) + "" + mave;
                mave = Integer.parseInt(ChMave);
            }
        }

        mave = mave + 1;

        return mave;
    }

